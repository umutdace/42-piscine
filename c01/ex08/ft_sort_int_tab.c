#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int		i;
	int		j;
	int		val;

	i = 0;
	j = 0;
	while (i < size)
	{
		while (j < size)
		{
			if (tab[i] > tab[j])
			{
				val = tab[i];
				tab[i] = tab[j];
				tab[j] = val;
			}
			j++;
		}
		i++;
		j = i + 1;
	}
}
